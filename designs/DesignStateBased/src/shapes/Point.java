package shapes;

/**
 * Represents a point on the canvas.
 *
 * @see shapes.GraphicElement
 */

public class Point {

    public final int X;
    public final int Y;

    /**
     * Creates a new point.
     *
     * @param x The x-coordinate
     * @param y The y-coordinate
     */
    public Point(int x, int y) {
        this.X = x;
        this.Y = y;
    }

}
